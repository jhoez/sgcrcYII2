    <h4>Mision y Vision</h4>
    <p>
        La Fundación Bolivariana de Informática y Telemática (Fundabit)
        es un organismo adscrito al Ministerio del Poder Popular para la Educación (MPPE),
        constituido mediante el Decreto Nº 1.193, el 6 de febrero de 2001,
        publicado en la Gaceta Oficial Nº 37.137, el 9 de febrero de 2001.
        <br><br>
        Misión: Implementar innovaciones tecnológicas en aspectos educativos
        a través de la incorporación y uso de las Tecnologías de Información
        y Comunicación (TIC) como factor de desarrollo humano y social en apoyo a las políticas,
        estrategias y objetivos emanados del Ministerio del Poder Popular para la Educación (MPPE).
        <br><br>
        Visión: Disponer de espacios educativos provistos de recursos y herramientas basadas en las TIC,
        distribuidos en todo el territorio nacional enmarcados en los principios del sistema educativo bolivariano.
    </p>
</div>
